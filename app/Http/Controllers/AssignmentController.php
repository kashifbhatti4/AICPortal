    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $this->validate($request,[
            'assignment_name'=>'required',
            'total_points'=>'required',
            'description'=>'required',
            'due_date'=>'required',
        ]);

        $Assignment=new Assignment();
        $Assignment->assignment_name=$request->input('assignment_name');
        $Assignment->classes_id=1;
        $Assignment->total_points=$request->input('total_points');
        $Assignment->due_date=$request->input('due_date');
        $Assignment->description=$request->input('description');
        $Assignment->save();
        return redirect('/')->with('success','assignment created');
    }

