    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $this->validate($request,[
            'users_id'=>'required',
            'assignment_id'=>'required',
            'grade'=>'required',
        ]);

        $Graded=new Graded();
        $Graded->users_id=Auth::id();
        $Graded->assignment_id=$request->input('assignment_id');
        $Graded->grade=$request->input('grade');
        $Graded->save();
        return redirect('/')->with('success','Grades sucessfully saved');
    }

